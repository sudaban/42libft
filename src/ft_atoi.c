#include "libft.h"

int	ft_atoi(const char *str)
{
	int	state;
	int	result;

	state = 1;
	result = 0;
	while (*str == '\t' || *str == '\n' || *str == '\v'
		|| *str == '\f' || *str == '\r' || *str == ' ')
		str++;
	if (*str == '-')
	{
		state *= -1;
		str++;
	}
	else if (*str == '+')
		str++;
	if (*str == '-' || *str == '+')
		return (0);
	while (*str >= '0' && *str <= '9')
	{
		result = (result * 10);
		result = result + (*str - '0');
		str++;
	}
	return (result * state);
}
